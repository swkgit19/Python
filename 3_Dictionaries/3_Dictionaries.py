print("\nDictionary:")
y = {"name": "Peter", "age": 31, "hobby": "Soccer"}
y1 = {"name": "Peter", "age": 31, "hobby": "Soccer"}['name']
y2 = {"name": "Peter", "age": 31, "hobby": "Soccer"}['age']
y3 = {"name": "Peter", "age": 31, "hobby": "Soccer"}['hobby']

print("{\"name\": \"Peter\", \"age\": 31, \"hobby\": \"Soccer\"} = " , y, "\n")
print("{\"name\": \"Peter\", \"age\": 31, \"hobby\": \"Soccer\"}['name'] = " , y1, "\n")
print("{\"name\": \"Peter\", \"age\": 31, \"hobby\": \"Soccer\"}['age'] = " , y2, "\n")
print("{\"name\": \"Peter\", \"age\": 31, \"hobby\": \"Soccer\"}['hobby'] = " , y3, "\n")
